package edu.tamu.tcat.analytics.datatrax;

public class TransformerConfigurationException extends Exception
{

   public TransformerConfigurationException()
   {
   }

   public TransformerConfigurationException(String message)
   {
      super(message);
   }

   public TransformerConfigurationException(Throwable cause)
   {
      super(cause);
   }

   public TransformerConfigurationException(String message, Throwable cause)
   {
      super(message, cause);
   }

   public TransformerConfigurationException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace)
   {
      super(message, cause, enableSuppression, writableStackTrace);
   }

}
